package Strings;

public class NumbersAndStrings {
    public static void main(String[] args) {
        // Numbers and Strings
        int x = 10;
        int y = 20;
        int z = x + y;
        System.out.println(z);

        String a = "10";
        int b = 20;
        String c = a + b;
        System.out.println(c);
    }
}
