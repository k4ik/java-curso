package Strings;

public class SpecialCharacters {
    public static void main(String[] args) {
        /*
            \'	'	Single quote
            \"	"	Double quote
            \\	\	Backslash
         */
        String txt = "We are the so-called \"Vikings\" from the north.";
        System.out.println(txt);

        /*
            \n	New Line	
            \r	Carriage Return	
            \t	Tab	
            \b	Backspace	
            \f	Form Feed
         */


    }
}
